# This program will check whether the two inputs(values/whole numbers) entered from the user are equal or not
# Important information: This program only works with whole numbers, it will not work with decimals.
a = int(input("Enter value/whole number: "))  # User input
b = int(input("Enter another value/whole number: "))  # User input
if a == b:  # Checks if value inserted for a is equal to the value inserted for b.
    print(" The Values/whole numbers you entered are equal")  # If it is it will print this.
else:       # If not
    print("The Values/whole numbers you entered are not equal")  # It will print this.
